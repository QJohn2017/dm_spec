def idx_ppar( directions, idx, coo ):
    """ Return a pair [ index, data_name ]"""
    cood = ["x", "y", "z"]
    return [ directions.index(idx) * 6 + cood.index(coo) * 2, 
             "[%s] in %s-axis" % 
             ( ', '.join( map( str, idx ) ), coo ) ]
